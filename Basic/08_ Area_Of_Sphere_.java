//Write A Program To Print The Area Of Sphere!!
import java.lang.*;
import java.util.*;
class AOS{
  public static void main(String args[]){
    Scanner sc=new Scanner(System.in);
    double Area_Of_Sphere;
    double pi=3.14;
    int r;
    System.out.println("Enter The Radius Of Sphere :");
    r=sc.nextInt();
    Area_Of_Sphere = 4*pi*r*r;
    System.out.println(" Area_Of_Sphere is "+  Area_Of_Sphere );
  }
}

